///////////////////////////////////////////
// SetBoard                              //
void GameBoard::SetStage( int stageNumber )
///////////////////////////////////////////
{
    if( stageNumber < 0 )
        stageNumber= 0;

    if( stageNumber >= MAX_STAGE )
        stageNumber= MAX_STAGE -1;

    for( int y= 0 ; y < GAMEBOARD_HEIGHT ; y++ )
    {
        for( int x= 0 ; x < GAMEBOARD_WIDTH ; x++ )
        {
            board[y][x]= boardDefault[stageNumber][y][x];
        }
    }

    return;
}

//////////////////////////////////
// CheckLine                    //
bool GameBoard::CheckLine( int y )
//////////////////////////////////
{
    for( int x= 1 ; x < GAMEBOARD_WIDTH - 1 ; x++ )
    {
        if( board[y][x] == 0 )
            return false;
    }
    return true;
}

//////////////////////////////////
// ClearLine                    //
void GameBoard::ClearLine( int y )
//////////////////////////////////
{
    int cleared= 0;

    for( ; y > 1 ; y-- )
    {
        for( int x = 1 ; x < GAMEBOARD_WIDTH - 1 ; x++ )
        {
            if( ( board[y][x] != 1 ) && ( board[y][x] != 2 ) )
            {
                if( ( board[y-1][x] != 1 ) && ( board[y-1][x] != 2 ) )
                {
                    board[y][x]= board[y-1][x];
                }
            }
        }
    }
}

